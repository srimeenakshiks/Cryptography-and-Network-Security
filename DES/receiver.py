    if len(key_hex.replace(" ", "")) != 14:
        print("Error: Key must be exactly 56 bits (14 hex characters).")
        receiver_socket.close()
        return

    decrypted_message = des_decrypt(encrypted_message, key_hex)
    print(f"Decrypted Message (Hex): {decrypted_message}")
    
    receiver_socket.close()

